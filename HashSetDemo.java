package DataStructures_Collections;
import java.util.*;


class HashSetDemo {
	public static void main(String[] args) {
		HashSet<String> hs = new HashSet<String>();
		
		hs.add("Alpha");
		hs.add("Beta");
		hs.add("gamma");
		hs.add("eta");
		hs.add("epsilon");
		hs.add("omega");
		
		System.out.println(hs);
	}

}
